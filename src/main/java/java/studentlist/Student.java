public class Student { // model for student info
   private String name;
   private int id;
   private int sem;
    
    public String getName() {
        return name;
    }

   
    public void setName(String name) {
        this.name = name;
    }

    
    public int getId() {
        return id;
    }

  
    public void setId(int id) {
        this.id = id;
    }

    
    public int getSem() {
        return sem;
    }

   
    public void setSem(int sem) {
        this.sem = sem;
    }
}
